package org.example.pojo;

public class Emp {
    private String name;
    private String job;

    public String getName()
    {
        return name;

    }

    // Setter
    public void setName(String newName)
    {
        this.name = newName;
    }
    public String getJob()
    {
        return job;
    }

    // Setter
    public void setJob(String newName)
    {
        this.job = newName;
    }
}
