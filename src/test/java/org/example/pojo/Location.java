package org.example.pojo;

public class Location {
    private double lat;
    private double lng;
    public double getLat() {
        return lat;
    }

    // Setter
    public void setLat(double newName) {
        this.lat = newName;
    }
    public double getLng() {
        return lng;
    }

    // Setter
    public void setLng(double newName) {
        this.lng = newName;
    }
}
